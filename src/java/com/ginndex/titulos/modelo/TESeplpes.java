public class TESeplpes {

    private String ID_Certificado;
    private String Version;
    private String FolioDigital;
    private String FechaSeplpes;
    private String SelloDreoe;
    private String NoCertificadoSepIpes;
    private String SelloSepIpes;

    public String getID_Certificado() {
        return ID_Certificado;
    }

    public void setID_Certificado(String ID_Certificado) {
        this.ID_Certificado = ID_Certificado;
    }

    public String getVersion() {
        return Version;
    }

    public void setVersion(String Version) {
        this.Version = Version;
    }

    public String getFolioDigital() {
        return FolioDigital;
    }

    public void setFolioDigital(String FolioDigital) {
        this.FolioDigital = FolioDigital;
    }

    public String getFechaSeplpes() {
        return FechaSeplpes;
    }

    public void setFechaSeplpes(String FechaSeplpes) {
        this.FechaSeplpes = FechaSeplpes;
    }

    public String getSelloDreoe() {
        return SelloDreoe;
    }

    public void setSelloDreoe(String SelloDreoe) {
        this.SelloDreoe = SelloDreoe;
    }

    public String getNoCertificadoSepIpes() {
        return NoCertificadoSepIpes;
    }

    public void setNoCertificadoSepIpes(String NoCertificadoSepIpes) {
        this.NoCertificadoSepIpes = NoCertificadoSepIpes;
    }

    public String getSelloSepIpes() {
        return SelloSepIpes;
    }

    public void setSelloSepIpes(String SelloSepIpes) {
        this.SelloSepIpes = SelloSepIpes;
    }

}
